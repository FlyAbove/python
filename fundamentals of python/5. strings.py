 # Strings are any character written between " "
 
 # We can concatenate strings using + operator

string = "name"
string += "second name"

print(string)

# Another way to concatenate strings is written down below

name = "John"
last_name = "Smith"
full_name = name + " " + last_name

print(full_name)

